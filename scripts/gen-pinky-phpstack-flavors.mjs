/**
 * Builds `src/data/pinkyImportedCloudwaysDetails.generated.ts` from
 * `src/data/phpstackProductsFrozen.json` (snapshot of Cloudways PHP shop API)
 * + `scripts/pinky-phpstack-catalog-rules.json`.
 *
 * Run: npm run gen:pinky-flavors
 *
 * To refresh the snapshot from the live API (needs credentials, not committed):
 *   PHPSTACK_SHOP_USER=... PHPSTACK_SHOP_PASS=... node scripts/refresh-phpstack-products-frozen.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const FROZEN = path.join(ROOT, "src", "data", "phpstackProductsFrozen.json");
const RULES = path.join(ROOT, "scripts", "pinky-phpstack-catalog-rules.json");
const CATALOG = path.join(ROOT, "src", "data", "pinkyImportedCatalog.ts");
const OUT = path.join(ROOT, "src", "data", "pinkyImportedCloudwaysDetails.generated.ts");

function parseCatalogIds(ts) {
  const ids = [];
  const re = /id:\s*"([^"]+)"/g;
  let m;
  while ((m = re.exec(ts)) !== null) {
    if (!ids.includes(m[1])) ids.push(m[1]);
  }
  return ids;
}

function flavorLabel(brandName, productName) {
  let s = String(productName).trim();
  const b = String(brandName).trim();
  if (s.startsWith(b)) s = s.slice(b.length).trim();
  s = s.replace(/[-－]?\s*1盒\s*3顆.*$/u, "").trim();
  s = s.replace(/[-－]?\s*1盒\s*3入.*$/u, "").trim();
  return s || String(productName).trim();
}

function rowMatches(brandName, productName, rule) {
  const bn = brandName.trim();
  const pn = productName;
  const brands = rule.brandNames.map((x) => x.trim());
  if (!brands.includes(bn)) return false;
  for (const ex of rule.excludeNameSubstrings || []) {
    if (pn.includes(ex)) return false;
  }
  const inc = rule.includeNameSubstrings;
  if (inc && inc.length) {
    if (!inc.some((s) => pn.includes(s))) return false;
  }
  return true;
}

function main() {
  const catalogSrc = fs.readFileSync(CATALOG, "utf8");
  const catalogIds = parseCatalogIds(catalogSrc);
  const rules = JSON.parse(fs.readFileSync(RULES, "utf8"));
  const rows = JSON.parse(fs.readFileSync(FROZEN, "utf8"));

  const byId = Object.fromEntries(catalogIds.map((id) => [id, new Set()]));

  for (const row of rows) {
    const brandName = row.brand_name;
    const productName = row.product_name;
    for (const id of catalogIds) {
      const rule = rules[id];
      if (!rule) continue;
      if (!rowMatches(brandName, productName, rule)) continue;
      const label = flavorLabel(brandName, productName);
      if (label) byId[id].add(label);
    }
  }

  const sortedIds = [...catalogIds].sort();
  const body = sortedIds
    .map((id) => {
      const flavors = [...byId[id]].sort((a, b) => a.localeCompare(b, "zh-Hant"));
      const inner = flavors.map((f) => JSON.stringify(f)).join(", ");
      return `  ${JSON.stringify(id)}: { flavors: [${inner}] }`;
    })
    .join(",\n");

  const ts = `// AUTO-GENERATED by scripts/gen-pinky-phpstack-flavors.mjs — do not edit by hand.
// Source: src/data/phpstackProductsFrozen.json + scripts/pinky-phpstack-catalog-rules.json
// Regenerate: npm run gen:pinky-flavors

export type PinkyImportedCloudwaysDetail = {
  flavors: readonly string[];
};

export const pinkyImportedCloudwaysDetailsById: Record<string, PinkyImportedCloudwaysDetail> = {
${body}
};
`;
  fs.writeFileSync(OUT, ts, "utf8");
  console.log(`[gen-pinky-flavors] wrote ${OUT}`);
  for (const id of sortedIds) {
    const n = byId[id].size;
    if (n) console.log(`  ${id}: ${n} flavors`);
  }
}

main();
